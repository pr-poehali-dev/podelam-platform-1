export type CareerTestResult = {
  scores: Record<CareerType, number>;
  topType: CareerType;
  secondType: CareerType;
  topScore: number;
  professions: string[];
};

export function calcCareerResult(answers: CareerType[]): CareerTestResult {
  const scores: Record<CareerType, number> = {
    creative: 0, analytical: 0, social: 0, tech: 0, leader: 0, nature: 0,
  };
  for (const a of answers) scores[a]++;

  const sorted = (Object.entries(scores) as [CareerType, number][]).sort((a, b) => b[1] - a[1]);
  const topType = sorted[0][0];
  const secondType = sorted[1][0];
  const topScore = Math.round((sorted[0][1] / answers.length) * 100);

  // –ü—Ä–æ—Ñ–µ—Å—Å–∏–∏: —Ç–æ–ø–æ–≤—ã–µ –∏–∑ –ø–µ—Ä–≤–æ–≥–æ —Ç–∏–ø–∞ + 2 –∏–∑ –≤—Ç–æ—Ä–æ–≥–æ
  const professions = [
    ...PROFESSIONS_BY_TYPE[topType].slice(0, 5),
    ...PROFESSIONS_BY_TYPE[secondType].slice(0, 2),
  ];

  return { scores, topType, secondType, topScore, professions };
}
